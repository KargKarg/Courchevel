
def code_to_string(code):
    if code == 'VVV':
        return 'piste verte'

    elif code == 'RRR':
        return 'piste rouge'

    elif code == 'NNN':
        return 'piste noire'

    elif code == 'TPH':
        return 'téléphérique'

    elif code == 'TCB':
        return 'télécabine'

    elif code == 'TSG':
        return 'télésiège'

    elif code == 'TSI':
        return 'téléski'
